export function getArtworkById(id) {
  return artworks.find((artwork) => artwork.id.toString() === String(id)) ?? artworks[0]
}

export function getRelatedArtworks(id, limit = 3) {
  const current = getArtworkById(id)
  const sameCategory = artworks.filter(
    (artwork) => artwork.id !== current.id && artwork.category === current.category,
  )
  const others = artworks.filter(
    (artwork) => artwork.id !== current.id && artwork.category !== current.category,
  )
  return [...sameCategory, ...others].slice(0, limit)
}

export function normalizeBaseUrl(baseUrl) {
  return String(baseUrl || "http://localhost:3000").replace(/\/$/, "")
}

export function getArtworkShareMetadata(id, baseUrl) {
  const artwork = getArtworkById(id)
  const origin = normalizeBaseUrl(baseUrl)
  return {
    title: `${artwork.title} | ${siteName}`,
    description: artwork.summary || artwork.description,
    url: `${origin}/gallery/${artwork.id}`,
    image: `${origin}${artwork.image}`,
  }
}
